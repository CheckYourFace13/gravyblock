/**
 * One-time cleanup: removes fake/stub data created by old automation logic.
 * Safe to run multiple times (all deletes are conditional).
 *
 * Usage: node scripts/cleanup-stub-data.mjs
 */

import { createRequire } from "node:module";
const require = createRequire(import.meta.url);

try { require("dotenv").config(); } catch {}

if (!process.env.DATABASE_URL) {
  console.error("ERROR: DATABASE_URL is not set.");
  process.exit(1);
}

const postgres = require("postgres");
const sql = postgres(process.env.DATABASE_URL, { ssl: "require", max: 1 });

console.log("Starting stub data cleanup...\n");

const [backlinks] = await sql`
  DELETE FROM backlink_opportunities
  WHERE source_name LIKE ${"Authority opportunity%"}
  RETURNING id
`;
console.log(`✓ Deleted fake backlink opportunity rows`);

const [drafts] = await sql`
  DELETE FROM published_content
  WHERE channel = 'internal_draft'
     OR body LIKE ${"Internal draft generated by recurring automation%"}
  RETURNING id
`;
console.log(`✓ Deleted internal_draft published content records`);

const [stubPublished] = await sql`
  DELETE FROM published_content
  WHERE title LIKE ${"Story %: local demand update"}
     OR title LIKE ${"Service-area story %: local proof update"}
     OR title LIKE ${"Draft article/page %"}
  RETURNING id
`;
console.log(`✓ Deleted stub published content records`);

const [stubQueue] = await sql`
  DELETE FROM content_queue
  WHERE title LIKE ${"Story %: local demand update"}
     OR title LIKE ${"Service-area story %: local proof update"}
  RETURNING id
`;
console.log(`✓ Deleted stub content queue items`);

const [monitors] = await sql`
  DELETE FROM citation_monitors
  WHERE source_name LIKE ${"Listing consistency monitor%"}
  RETURNING id
`;
console.log(`✓ Deleted placeholder citation monitor records`);

const [syntheticAI] = await sql`
  DELETE FROM ai_visibility_checks
  WHERE engine = 'synthetic'
     OR prompt LIKE ${"recurring local-intent autopilot probe%"}
  RETURNING id
`;
console.log(`✓ Deleted synthetic AI visibility check records`);

await sql.end();
console.log("\nCleanup complete.");
